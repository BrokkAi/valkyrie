fn parse_bool(value: &str) -> Option<bool> {
    match value.trim().to_ascii_lowercase().as_str() {
        "1" | "true" | "yes" | "on" => Some(true),
        "0" | "false" | "no" | "off" => Some(false),
        _ => None,
    }
}

fn is_multi_value_key(key: &str) -> bool {
    matches!(key, "validation.command")
}

fn yaml_scalar(value: &str) -> String {
    if value
        .chars()
        .all(|item| item.is_ascii_alphanumeric() || matches!(item, '-' | '_' | '.'))
    {
        value.to_string()
    } else {
        format!("\"{}\"", value.replace('\\', "\\\\").replace('"', "\\\""))
    }
}

#[cfg(test)]
mod tests {
    use std::fs;
    use std::time::{SystemTime, UNIX_EPOCH};

    use super::{DefaultsFile, repo_defaults_path};

    #[test]
    fn defaults_roundtrip() {
        let temp = unique_temp_dir("defaults");
        fs::create_dir_all(temp.join(".valkyrie")).expect("create");
        let path = repo_defaults_path(&temp);

        let mut defaults = DefaultsFile::default();
        defaults.set("repo.base", "main".into());
        defaults.set("validation.command", "cargo test".into());
        defaults.set("validation.command", "cargo fmt --check".into());
        defaults.save(&path).expect("save");

        let loaded = DefaultsFile::load(&path).expect("load");
        assert_eq!(
            loaded.get("repo.base").expect("repo base"),
            &vec!["main".to_string()]
        );
        assert_eq!(
            loaded.get("validation.command").expect("validation").len(),
            2
        );
    }

    fn unique_temp_dir(name: &str) -> std::path::PathBuf {
        let suffix = SystemTime::now()
            .duration_since(UNIX_EPOCH)
            .expect("time")
            .as_nanos();
        std::env::temp_dir().join(format!("valkyrie-{name}-{suffix}"))
    }
}
